// Print out numbers from 0 to 10 using a for loop.

#include<iostream>
// #include<string>
using namespace std;

int main() {

    for (int i = 0; i <= 10; i++) {

        cout << i << endl;
    }

    // Print out "Yes" 5 times using a for loop.

    for (int i = 0; i <= 5; i++) {
        cout << "Yes" << endl;
    }

    return 0;
}

